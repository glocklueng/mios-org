/////////////////////////////////////////////////////////////////////////////
// This function is called by MIOS when the display content should be 
// initialized. Thats the case during startup and after a temporary message
// has been printed on the screen
/////////////////////////////////////////////////////////////////////////////
void DISPLAY_Init(void) __wparam
{
  unsigned char error;

  MIOS_LCD_Clear();
  MIOS_LCD_CursorSet(0x00);
  MIOS_LCD_PrintCString("EEPROM programmed");
  MIOS_LCD_CursorSet(0x40);
  MIOS_LCD_PrintCString("Status: ");
  MIOS_LCD_PrintHex2(error=EEPROM_CONTENT_Write());

  // send status as Program Change message for the case that no LCD is connected
  MIOS_MIDI_TxBufferPut(0xc0 | ((error & 0x80) ? 1 : 0));
  MIOS_MIDI_TxBufferPut(error & 0x7f);
}

